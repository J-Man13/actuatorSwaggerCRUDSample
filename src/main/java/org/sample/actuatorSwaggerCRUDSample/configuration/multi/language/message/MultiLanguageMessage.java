package org.sample.actuatorSwaggerCRUDSample.configuration.multi.language.message;

public class MultiLanguageMessage {
    private String en;
    private String ru;
    private String az;

    public MultiLanguageMessage(){

    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getRu() {
        return ru;
    }

    public void setRu(String ru) {
        this.ru = ru;
    }

    public String getAz() {
        return az;
    }

    public void setAz(String az) {
        this.az = az;
    }
}
